package ar.edu.unlp.info.oo1.ej9_AlquilerAutomobiles;

public class AutoEnAlquiler {
	private double precioPorDia; 
	private int cantidadPlazas;
	private String marca;
	
	
	public double getPrecioPorDia() {
		return precioPorDia;
	}
	public void setPreciPorDia(double preciPorDia) {
		this.precioPorDia = preciPorDia;
	}
	public int getCantidadPlazas() {
		return cantidadPlazas;
	}
	public void setCantidadPlazas(int cantidadPlazas) {
		this.cantidadPlazas = cantidadPlazas;
	}
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	
	
}
